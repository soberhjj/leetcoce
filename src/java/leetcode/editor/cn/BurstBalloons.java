            //dp[i][j]å«ä¹‰ï¼šè¡¨ç¤ºå¼€åŒºé—´ï¼ˆi,jï¼‰èƒ½æ‹¿åˆ°çš„æœ€å¤šé‡‘å¸æ•°ï¼Œå³ä»ç¬¬iæ°”çƒåˆ°ç¬¬jä¸ªæ°”çƒï¼ˆä¸åŒ…æ‹¬iå’Œjè¿™ä¸¤ä¸ªæ°”çƒï¼‰æ‰€èƒ½æ‹¿åˆ°çš„æœ€å¤šé‡‘å¸æ•°
            int[][] dp = new int[n + 2][n + 2];
